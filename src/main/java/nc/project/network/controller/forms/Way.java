package nc.project.network.controller.forms;

public class Way {
    private String firstVertex;

    private String secondVertex;

    private int parameterWay;

    public String getFirstVertex() {
        return firstVertex;
    }

    public void setFirstVertex(String firstVertex) {
        this.firstVertex = firstVertex;
    }

    public String getSecondVertex() {
        return secondVertex;
    }

    public void setSecondVertex(String secondVertex) {
        this.secondVertex = secondVertex;
    }

    public int getParameterWay() {
        return parameterWay;
    }

    public void setParameterWay(int parameterWay) {
        this.parameterWay = parameterWay;
    }
}
